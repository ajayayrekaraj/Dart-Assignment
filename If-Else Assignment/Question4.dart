/* Program 5 :
Write a dart program to check if a character is a vowel or
consonant.
Input: var=”A”;
Output: A is a vowel.
Input: var=”D”;
Output: D is a consonant */

void main() {
  var x = "D";
  if (x == "A" || x == "E" || x == "I" || x == "O" || x == "U") {
    print("It is a vowel");
  } else {
    print("It is consonant");
  }
}
